public class JavaInt {
    public static void main (String[] args) {

        int a = 2;              // int is a whole number
        double b = 3.55;        // double is a big decimal number.
        double c = (b*a+b);
        float d = 2.5F;         // Float is a small decimal number.
        long e = 123456L;       // Long is a big whole number.

        System.out.println(a*c);
        System.out.println(c*a/b);

        System.out.println("\n"); // space between line

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
